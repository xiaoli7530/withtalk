@SuppressWarnings("serial") 
@Entity
@Table(name = "BASE_COUNTRYS")
@BatchSize(size = 20)
public class BaseCountrys extends BaseEntity implements Serializable {

	@Id
	@GeneratedValue(generator = "system-uuid")
	@GenericGenerator(name = "system-uuid", strategy = "uuid")
	@Column(name = "COUNTRY_UUID")
	private String countryUuid;//uuid

	@Column(name = "COUNTRY_NAME")
	private String countryName;//国家名称

	@Column(name = "COUNTRY_CODE")
	private String countryCode;//国家代码

	@Column(name = "PINYIN")
	private String pinyin;//拼音

	@Column(name = "SEQ_NO")
	private Long seqNo;//排序号

	public String getCountryUuid() {
		return countryUuid;
	}

	public void setCountryUuid(String countryUuid) {
		this.countryUuid = countryUuid;
	}
	
	public String getCountryName() {
		return countryName;
	}

	public void setCountryName(String countryName) {
		this.countryName = countryName;
	}
	
	public String getCountryCode() {
		return countryCode;
	}

	public void setCountryCode(String countryCode) {
		this.countryCode = countryCode;
	}
	
	public String getPinyin() {
		return pinyin;
	}

	public void setPinyin(String pinyin) {
		this.pinyin = pinyin;
	}
	
	public Long getSeqNo() {
		return seqNo;
	}

	public void setSeqNo(Long seqNo) {
		this.seqNo = seqNo;
	}
	
}

